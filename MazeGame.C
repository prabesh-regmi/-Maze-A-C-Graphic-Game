void check()
{
    clearviewport();
    setcolor(GREEN);
    for(i=r-2; i>=0; i--)
	circle(x,y,i);
    for(i=r; i>=r-1; i--)
	circle(x1,y1,i);
    setcolor(BLUE);
    board_hard();
    rule();
    draw_line();
    while(key!='e'&&key!=Esc)
    {
	while(!kbhit())
	{
	    for_time();
	}
	key=getch();
	time_c=time_c+50;

	if(key=='a'||key==LEFT)
	{
	    i=0;
	    p=0;
	    while(i!=500&&p!=1)
	    {

		if(x==a[i]+r&&y>=b[i]&&y<=d[i]||x<=r)
		{
		    p++;
		}
		i++;
	    }
	    if(p==1)
		x=x;
	    else
	    {
		x=x-2*r;
		move++;

		move_sound();
	    }
	}
	else if(key=='d'||key==RIGHT)
	{
	    i=0;
	    p=0;
	    while(i!=500&&p!=1)
	    {

		if(x==a[i]-r&&y>=b[i]&&y<=d[i])
		{
		    p++;
		}
		i++;
	    }
	    if(p==1)
		x=x;
	    else
	    {
		x=x+2*r;
		move++;

		move_sound();
	    }
	}
	else if(key=='w'||key==UP)
	{
	    i=0;
	    p=0;
	    while(i!=500&&p!=1)
	    {


		if(y==b[i]+r&&x>=a[i]&&x<=c[i])
		{
		    p++;
		}
		i++;
	    }
	    if(p==1)
		y=y;
	    else
	    {
		y=y-2*r;
		move++;

		move_sound();
	    }
	}
	else if(key=='s'||key==DOWN)
	{
	    i=0;
	    p=0;
	    while(i!=500&&p!=1)
	    {

		if(y==b[i]-r&&x>=a[i]&&x<=c[i])
		{
		    p++;
		}
		i++;
	    }
	    if(p==1)
		y=y;
	    else
	    {
		y=y+2*r;
		move++;

		move_sound();
	    }
	}
	clearviewport();
	setcolor(GREEN);
	for(i=r-2; i>=0; i--)
	    circle(x,y,i);
	for(i=r; i>=r-1; i--)
	    circle(x1,y1,i);
	setcolor(BLUE);
	board_hard();
	rule();
	draw_line();
	if(abcd==10)
	{
	outtextxy(200,150,"Sorry! Time Over");
	outtextxy(200,200,"Try Again");
	key='e';
	}
	if(x==x1&&y==y1)
	{
	    for_sound();
	    key='e';
	}
    }
}



void board_hard()
{
    i=0;
    p=0;
    while(p!=1)
    {
	if(a[i]==0&&b[i]==0&&c[i]==0&&d[i]==0)
	    p++;

	line(a[i],b[i],c[i],d[i]);
	if(a[i]==c[i])
	{
	    line(a[i]+1,b[i],c[i]+1,d[i]);
	}
	else
	{
	    line(a[i],b[i]+1,c[i],d[i]+1);
	}
	i++;
    }
}

