void draw_line()
{
    setcolor(RED);
    if(z==0)
    {
	q[z]=x;
	w[z]=y;
	e[z]=x;
	t[z]=y;
	z++;
    }
    else
    {
	q[z]=x;
	w[z]=y;
	e[z]=q[z-1];
	t[z]=w[z-1];
	if(q[z]==q[z-1]&&w[z]==w[z-1])
	    z=z;
	else
	    z++;
	if(q[z]==q[z-2]&&w[z]==w[z-2])
	{
	    z--;
	}
    }

    for(m=0; m<z; m++)
    {
	if(q[m]==e[m]&&w[m]>t[m])
	    line(q[m],w[m]-r,e[m],t[m]);
	else if(w[m]==t[m]&&q[m]>e[m])
	    line(q[m]-r,w[m],e[m],t[m]);
	else if(q[m]==e[m]&&w[m]<t[m])
	    line(q[m],w[m],e[m],t[m]-r);
	else if(w[m]==t[m]&&q[m]<e[m])
	    line(q[m],w[m],e[m]-r,t[m]);


    }
    setcolor(BLUE);
}
